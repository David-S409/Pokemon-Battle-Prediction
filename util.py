def calculate_type_advantages(row):
    type1_advantages = sum(
        new_all_types[row["Type1"]][t] for t in new_all_types if t != "None"
    )
    type2_advantages = sum(
        new_all_types[row["Type2"]][t] for t in new_all_types if t != "None"
    )
    return type1_advantages + type2_advantages


